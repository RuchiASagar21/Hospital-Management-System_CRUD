    private void submitbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitbtnActionPerformed
        String name= nametxt.getText();
        int age= Integer.parseInt(agetxt.getText());
        if(btnmale.isSelected()==true)
        {
            gender = "Male";
        }
        if(btnfemale.isSelected()==true)
        {
            gender = "Female";
        }
        if(btnothers.isSelected()==true)
        {
            gender = "Others";
        }
        String emailid= emailidtxt.getText();
        long contactno= Long.parseLong(contactnotxt.getText());
        String password= passwordtxt.getText();
        String confirmpassword= confirmpasswordtxt.getText();

        Person p =personHistory.addPerson();
        p.setName(name);
        p.setAge(age);
        p.setGender(gender);
        p.setEmailAdd(emailid);
        p.setContactNo(contactno);
        p.setPassword(password);
        p.setConfirmpassword(confirmpassword);

        JOptionPane.showMessageDialog(this, "Registered Successfully!!");
        PatientJFrame pat=new PatientJFrame(personHistory,patientHistory);
        pat.namelblpat.setText(nametxt.getText());
        pat.agelblpat.setText(agetxt.getText());
        pat.genderlblpat.setText(gender);
        pat.emailidlblpat.setText(emailidtxt.getText());
        pat.contactnolblpat.setText(contactnotxt.getText());

        pat.setVisible(true);

        nametxt.setText("");
        agetxt.setText("");
        btngender.clearSelection();
        emailidtxt.setText("");
        contactnotxt.setText("");
        passwordtxt.setText("");
        confirmpasswordtxt.setText("");        // TODO add your handling code here:
    }//GEN-LAST:event_submitbtnActionPerformed

