    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String name = txtName.getText();
        int empId = Integer.parseInt(txtEmpID.getText());
        int age = Integer.parseInt(txtAge.getText());
        //String gender = txtGender.getText();
        if(btnmale.isSelected()==true)
        {
            gender = "Male";
        }
        if(btnfemale.isSelected()==true)
        {
            gender = "Female";
        }
        if(btnothers.isSelected()==true)
        {
            gender = "Others";
        }
        SimpleDateFormat d = new SimpleDateFormat("MM-dd-yyyy");
        String date = d.format(jDateChooser1.getDate());
        String level = (String) Listlevel.getSelectedItem();
        String teamInfo = txtTeamInfo.getText();
        String positionTitle = txtPositionTitle.getText();
        Long phoneNo = Long.parseLong(txtContactNo.getText());
        String emailAdd = txtEmailID.getText();
        String empPhoto = lblphoto.getText();
        
        if(!(emailAdd.contains("@"))||!(emailAdd.endsWith(".com")))
        {
            JOptionPane.showMessageDialog(this, "Please enter correct details");
        }
        else
        {
        JOptionPane.showMessageDialog(this, "Valid Email Id!! ");}
        
        
         
        
        EmpData vs = history.addNewEmp();
        vs.setName(name);
        vs.setEmpId(empId);
        vs.setAge(age);
        vs.setGender(gender);
        vs.setDate(date);
        vs.setLevel(level);
        vs.setTeamInfo(teamInfo);
        vs.setPositionTitle(positionTitle);
        vs.setPhoneNo(phoneNo);
        vs.setEmailAdd(emailAdd);
        vs.setEmpPhoto(empPhoto);
        
        JOptionPane.showMessageDialog(this,"Emp Data has been saved" );
        
        txtName.setText("");
        txtEmpID.setText("");
         txtAge.setText("");
          //txtGender.setText("");
          btngender.clearSelection();
           jDateChooser1.setDate(null);
           Listlevel.setSelectedItem(null);
            txtTeamInfo.setText("");
             txtPositionTitle.setText("");
              txtContactNo.setText("");
               txtEmailID.setText("");
                lblphoto.setIcon(null);
                
               
        
        
        
        
        
        
        
        
        
        
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

