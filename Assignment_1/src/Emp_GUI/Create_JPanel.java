    private void btnuploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnuploadActionPerformed
        // TODO add your handling code here:
        JFileChooser browseImageFile = new JFileChooser();
        FileNameExtensionFilter fnef = new FileNameExtensionFilter("Image","png","jpg","jpeg");
        browseImageFile.addChoosableFileFilter(fnef);
        int showOpenDialogue = browseImageFile.showOpenDialog(null);
        
        if (showOpenDialogue == JFileChooser.APPROVE_OPTION){
            File selectedImageFile = browseImageFile.getSelectedFile();
            String selectedImagePath = selectedImageFile.getAbsolutePath();
            JOptionPane.showMessageDialog(null, selectedImagePath);
            ImageIcon ii = new ImageIcon(selectedImagePath);
            Image Image = ii.getImage().getScaledInstance(lblphoto.getWidth(),getHeight(), SCALE_SMOOTH);
            lblphoto.setIcon(new ImageIcon(Image));
            
            
            
            
            
        }
        
        
        
        
    }//GEN-LAST:event_btnuploadActionPerformed

    private void txtContactNoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtContactNoKeyReleased
        // TODO add your handling code here:
        String phone=txtContactNo.getText();
        if(phone.matches("^[1-9]{1}[0-9]{9}$") ){
            txtContactNo.setBackground(java.awt.Color.green);
            txtContactNo.setEditable(false);
        }
        else{
         txtContactNo.setBackground(java.awt.Color.red);
         
        }
        
    }//GEN-LAST:event_txtContactNoKeyReleased

