    private boolean validateData1() {
                if (allergiestxtpat.getText().length() < 1) {
            JOptionPane.showMessageDialog(this, "Please Enter any allergies or Type NA");
            return false;
        }
                        if (emergencytxtpat.getText().length() < 1) {
            JOptionPane.showMessageDialog(this, "Emergency Contact number is required!!");
            return false;
        }
                        if (medicalhistorytxtpat.getText().length() < 1) {
            JOptionPane.showMessageDialog(this, "If No MedicalHistory type NA");
            return false;
        }
                       return true;}
    
    
    private void submitbtnpatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitbtnpatActionPerformed
        // TODO add your handling code here:
        
        boolean valid=validateData1();
        if(valid){
        String name= namelblpat.getName();
        int age= Integer.parseInt(agelblpat.getText());
        String gender= genderlblpat.getText();
        String emailid= emailidlblpat.getText();
        long contactno= Long.parseLong(contactnolblpat.getText());
        if(singlebtn.isSelected()==true)
        {
             maritalstatus = "Single";
        }
        if(marriedbtn.isSelected()==true)
        {
            maritalstatus = "Married";
        }
        if(yesbtn.isSelected()==true)
        {
             insurance = "Yes";
        }
        if(nobtn.isSelected()==true)
        {
            insurance = "No";
        }
        
        
        
        
        
        
         String allergies= allergiestxtpat.getText();
        long emergencycontact= Long.parseLong(emergencytxtpat.getText());
        String medicalhistory= medicalhistorytxtpat.getText();
        String symptoms=(String) symptomscbox.getSelectedItem();
        
        Patient pa =patientHistory.addNewPatient();
        pa.setName(name);
        pa.setAge(age);
        pa.setGender(gender);
        pa.setEmailid(emailid);
        pa.setContactno(contactno);
       
        pa.setMaritalstatus(maritalstatus);
        pa.setInsurance(insurance);
        pa.setAllergies(allergies);
        pa.setEmergencycontact(emergencycontact);
        pa.setMedicalhistory(medicalhistory);
        pa.setSymptoms(symptoms);
        JOptionPane.showMessageDialog(this, "Patient Registered Successfully!!");
        
       
  
        
        dispose();
        PatientHouseDetailsJFrame phd=new PatientHouseDetailsJFrame(personHistory, patientHistory,houseHistory,communityHistory,cityHistory,hospitalHistory,hospitalDirHistory,doctorHistory,encounterHistory,vitalSignsHistory);
        phd.namep.setText(namelblpat.getText());
        phd.agep.setText(agelblpat.getText());
        phd.genderp.setText(genderlblpat.getText());
        phd.emailidp.setText(emailidlblpat.getText());
        phd.contactnop.setText(contactnolblpat.getText());
        phd.maritalstatusp.setText(maritalstatus);
        phd.allergiesp.setText(allergiestxtpat.getText());
        phd.emergencycontactp.setText(emergencytxtpat.getText());
        phd.medhistp.setText(medicalhistorytxtpat.getText());
        phd.symptomsp.setText(symptoms);
        
        
        
        
        phd.setVisible(true);
        
       
        maritalstatusbtn.clearSelection();
        insurancebtn.clearSelection();
        allergiestxtpat.setText("");
        emergencytxtpat.setText("");
        medicalhistorytxtpat.setText("");
        symptomscbox.setSelectedIndex(-1);}
    }//GEN-LAST:event_submitbtnpatActionPerformed

