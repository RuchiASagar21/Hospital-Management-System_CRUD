    private void savebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savebtnActionPerformed
        // TODO add your handling code here:
        String temp= temptxt.getText();
       
        String bloodgroup= bloodgrouptxt.getText();
        int weight= Integer.parseInt(bodyweighttxt.getText());
        String oxygenlevel= oxygenleveltxt.getText();
        
        String bloodpressure= bloodpressuretxt.getText();
        String resprate= respratetxt.getText();
        
        
        VitalSigns vs=vitalSignsHistory.addNewVitalSigns();
        vs.setBodytemp(temp);
        vs.setBloodgroup(bloodgroup);
        vs.setWeight(weight);
        vs.setOxygenlevel(oxygenlevel);
       vs.setBloodpressure(bloodpressure);
       
        vs.setResprate(resprate);
        
        JOptionPane.showMessageDialog(this, "Vital Signs Created!!");
        
        dispose();
        EncounterJFrame enj=new EncounterJFrame(personHistory, patientHistory,houseHistory,communityHistory,cityHistory,hospitalHistory,hospitalDirHistory,doctorHistory,encounterHistory,vitalSignsHistory);
        enj.patnamelble.setText(patnamelbl.getText());
         enj.agelble.setText(agelbl.getText());
          enj.genderlble.setText(genderlbl.getText());
         enj.allergieslble.setText(allergieslbl.getText());
          enj.medhistlble.setText(medhistlbl.getText());
         enj.symptomslble.setText(symptomslbl.getText());
         enj.templble.setText(temptxt.getText());
         enj.bloodgrouplble.setText(bloodgrouptxt.getText());
         enj.weightlble.setText(bodyweighttxt.getText());
         enj.oxygenlble.setText(oxygenleveltxt.getText());
         enj.bloodpressurelble.setText(bloodpressuretxt.getText());
         enj.respratelble.setText(respratetxt.getText());
        enj.setVisible(true);
    }//GEN-LAST:event_savebtnActionPerformed

